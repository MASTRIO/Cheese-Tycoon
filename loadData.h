//// * Include
#include <iostream>
#include <fstream>

//// * Functions
// Cheese
void loadDataCheese() {
    // Make the array
    int arr[30];
    std::ifstream is("./jagac/currency_cheese.cheese");
    int cnt= 0;
    int x;

    // Make sure the array isn't full
    while (cnt < arr[30] && is >> x)

    // And that it can read the integer
    arr[cnt++] = x;

    // Saves the Data
    for (int i = 0; i < cnt; i++) {
        cheese = arr[i];
    }

    // ! Outputs Data
    std::cout << cheese << "\n";

    // Close the file
    is.close();
}

